    public void colocarFoto(JLabel label, BufferedImage imagen) {
        ImageIcon icono = new ImageIcon(imagen.getScaledInstance(label.getWidth(), label.getHeight(), Image.SCALE_SMOOTH));
        label.setIcon(icono);
    }

